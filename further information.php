<h2><b>Feeling faint</b></h2>
<b>Most people adapt quickly to donating blood, however if you feel faint we advise you to:</b>
<br>
1.drink plenty of fluids and have a salty snack
<br>2.eat your normal, regular meals
<br>3.avoid hot rooms and hot showers/baths
<br>4.avoid vigorous exercise or rushing about
<br>5.avoid standing for long periods
<h3><b>If you feel unwell:</b></h3>

<br>1.tell someone, either a friend or family member, if possible, and ask them to keep an eye on you
<br>2.lie down and raise your legs if you can.
<br>3.get up slowly when you feel well enough, and drink plenty of fluids
<h3><b>To reduce the chance of you feeling unwell at your next donation:</b></h3>

<br>1.on the day of donation and the evening before, drink plenty of fluids and eat normally, but avoid alcohol. as this will dehydrate you
<br>2.drink the full 500ml of water that you are asked to drink before you give blood - this reduces the chance of fainting
<br>3.ask staff to remind you about the applied muscle tension exercises
<br>4.tell staff about your previous experience of feeling faint or fainting.

